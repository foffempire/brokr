        $('#selectPlan').on('change',function (e) {
            e.preventDefault();
            "use strict"

            var id = $(this).val();

            if (id != 0) {

                var invest_amount = $("#enter-amount");
                invest_amount.val('');
                invest_amount.attr('readonly', false);

                var url = 'schema-selected.php?plan='+id;
                $.ajax({
                    url: url, success: function (result) {
                        result = $.parseJSON(result);
                        $('#amount-level').html('Capital Back: Yes');
                        $('#profit-label').html(result.percentage + '% (Daily) - 3 Times');
                        $('#profit').val(0);
                        $('#id-amount').val(0);
                    }
                });
            }

        })

        $('#enter-amount').on('keyup',function (e) {
            e.preventDefault();
            "use strict"
            var id = $('#selectPlan').val();

            if (id != 0) {
                var url = 'schema-selected.php?plan='+id;
                $.ajax({
                    url: url, success: function (result) {
                        result = $.parseJSON(result);
                        var invest_amount = $("#enter-amount").val();
                        if(invest_amount > 0){
                            $('#profit').val(calPercentage(invest_amount, result.percentage) * 3);
                        }
                    }
                });
            }

        })


